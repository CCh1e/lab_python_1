print("\n--- Завдання 3 ---")

try:
    n = int(input("Введіть ціле число N (1 < N < 9): "))

    # Перевірка умови 1 < N < 9
    if 1 < n < 9:
        print("Результат:")
        # Зовнішній цикл: кількість рядків
        for i in range(1, n + 1):
            # Внутрішній цикл: виведення чисел у рядку
            for j in range(1, i + 1):
                print(j, end=" ")
            print() # Перехід на новий рядок
    else:
        print("Число не входить у діапазон (1 < N < 9).")

except ValueError:
    print("Помилка: введіть ціле число.")
